public class Map {

    public Dictionary<Vector2, Rectangle> allTiles = new Dictionary<Vector2, Rectangle>();
    public List<Rectangle> normalTiles = new List<Rectangle>();
    public List<Rectangle> breakableTiles = new List<Rectangle>();
    public List<Rectangle> bossTiles = new List<Rectangle>();

    public Vector2 playerSpawn = Vector2.Zero;

    public enum Maps {
        map1,
        map2,
        map3
    }

    public void setMap(Maps map) {
        if (map == Maps.map1) {
            createMap(map1);
        }
        else if (map == Maps.map2) {
            createMap(map2);
        }
        else if (map == Maps.map3) {
            createMap(map3);
        }
    }

    private void createMap(int[,] map) {
        normalTiles = new List<Rectangle>();
        breakableTiles = new List<Rectangle>();
        bossTiles = new List<Rectangle>();

        Game game = Game.Instance;

        for (int y = 0; y < map.GetLength(0); y++)
            for (int x = 0; x < map.GetLength(1); x++) {
                switch(map[y, x]) {
                    case -1:
                        break;
                    case 0: {
                        Rectangle newTile = Tile.createTile(x, y);
                        normalTiles.Add(newTile);
                        allTiles.Add(new Vector2(x, y), newTile);
                        break;
                    }
                    case 1: {
                        Rectangle newTile = Tile.createTile(x, y);
                        breakableTiles.Add(newTile);
                        allTiles.Add(new Vector2(x, y), newTile);
                        break;
                    }
                    case 2: {
                        Rectangle newTile = Tile.createTile(x, y);
                        bossTiles.Add(newTile);
                        allTiles.Add(new Vector2(x, y), newTile);
                        break;
                    }
                    case 10:
                        game.player = new Player(x * Tile.TILE_WIDTH + 1, y * Tile.TILE_HEIGHT + 1);
                        playerSpawn = new Vector2(x * Tile.TILE_WIDTH + 1, y * Tile.TILE_HEIGHT + 1);
                        breakableTiles.Add(Tile.createTile(x, y));
                        break;  //create Player
                    case 11:
                        game.boss = new Boss(x * Tile.TILE_WIDTH, y * Tile.TILE_HEIGHT);
                        break; //create boss
                }
            }
        
        game.arenaWidth = map.GetLength(1) * Tile.TILE_WIDTH;
        game.arenaHeight = map.GetLength(0) * Tile.TILE_HEIGHT;

        Vector2 tilePadding = new Vector2();
        tilePadding.X = game.abyssTilePadding * Tile.TILE_WIDTH;
        tilePadding.Y = game.abyssTilePadding * Tile.TILE_HEIGHT;

        game.abyssStartPos = new Vector2(-tilePadding.X, -tilePadding.Y);
        game.abyssSize = new Vector2(game.arenaWidth + (tilePadding.X * 2), game.arenaHeight + (tilePadding.Y * 2));
        game.abyssScaleFactor = new Vector2(2 / game.abyssSize.X, 2 / game.abyssSize.Y);

        //game.camera.setVirtualScreen(game.arenaWidth, game.arenaHeight);
    }

    public void draw2D(Texture2D tileTexture) {
        drawTiles(normalTiles, tileTexture, Color.WHITE);
        drawTiles(breakableTiles, tileTexture, Color.RED);
        drawTiles(bossTiles, tileTexture, Game.Instance.boss.GetColor());
    }

    private void drawTiles(List<Rectangle> tiles, Texture2D tileTexture, Color tint) {
        foreach (Rectangle tile in tiles) {
            Raylib.DrawTexture(tileTexture, (int)tile.x, (int)tile.y, tint);
        }
    }

    public Vector2[] getTilesInRange(Vector2 point, int radius) {

        List<Vector2> tilePositions = new List<Vector2>();

        int startX = (int)point.X - radius;
        int startY = (int)point.Y - radius;

        int endX = (int)point.X + radius;
        int endY = (int)point.Y + radius;


        for (int x = startX; x < endX; x++)
            for (int y = startY; y < endY; y++) {
                if (allTiles.ContainsKey(new Vector2(x, y))){
                    tilePositions.Add(new Vector2(x, y));
                }
            }
        return tilePositions.ToArray();
    }

    public Vector2[] getMissingTilesInRange(Vector2 point, int radius) {

        List<Vector2> missingTilePositions = new List<Vector2>();

        int startX = (int)point.X - radius;
        int startY = (int)point.Y - radius;

        int endX = (int)point.X + radius;
        int endY = (int)point.Y + radius;


        for (int x = startX; x < endX; x++)
            for (int y = startY; y < endY; y++) {
                if (!allTiles.ContainsKey(new Vector2(x, y))){
                    missingTilePositions.Add(new Vector2(x, y));
                }
            }
        return missingTilePositions.ToArray();
    }

    public Vector2[] getTileRayCollisions(Ray ray) {

        List<Vector2> toReturn = new List<Vector2>();

        foreach (Rectangle tile in allTiles.Values) {

            Vector3 tileStart = new Vector3(tile.x, tile.y, 1);
            Vector3 tileEnd = new Vector3(tile.x + tile.width, tile.y + tile.width, -1);

            BoundingBox box = new BoundingBox(tileStart, tileEnd);

            if (Raylib.CheckCollisionRayBox(ray, box)) {
                toReturn.Add(new Vector2(tile.x, tile.y));
            }
        }

        return toReturn.ToArray();
    }









