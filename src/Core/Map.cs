public class Map {

    public Dictionary<Vector2, Rectangle> allTiles = new Dictionary<Vector2, Rectangle>();
    public List<Rectangle> normalTiles = new List<Rectangle>();
    public List<Rectangle> breakableTiles = new List<Rectangle>();
    public List<Rectangle> bossTiles = new List<Rectangle>();

    public enum Maps {
        map1,
        map2
    }

    public void setMap(Maps map) {
        if (map == Maps.map1) {
            createMap(map1);
        }
        else if (map == Maps.map2) {
            createMap(map2);
        }
    }

    private void createMap(int[,] map) {
        normalTiles = new List<Rectangle>();
        breakableTiles = new List<Rectangle>();
        bossTiles = new List<Rectangle>();

        Game game = Game.Instance;

        for (int y = 0; y < map.GetLength(0); y++)
            for (int x = 0; x < map.GetLength(1); x++) {
                switch(map[y, x]) {
                    case 0:
                        break;
                    case 1: {
                        Rectangle newTile = Tile.createTile(x, y);
                        normalTiles.Add(newTile);
                        allTiles.Add(new Vector2(x, y), newTile);
                        break;
                    }
                    case 2: {
                        Rectangle newTile = Tile.createTile(x, y);
                        breakableTiles.Add(newTile);
                        allTiles.Add(new Vector2(x, y), newTile);
                        break;
                    }
                    case 3: {
                        Rectangle newTile = Tile.createTile(x, y);
                        bossTiles.Add(newTile);
                        allTiles.Add(new Vector2(x, y), newTile);
                        break;
                    }
                    case 10:
                        game.player = new Player(x * Tile.TILE_WIDTH + 1, y * Tile.TILE_HEIGHT + 1);
                        breakableTiles.Add(Tile.createTile(x, y));
                        break;  //create Player
                    case 11:
                        game.boss = new Boss(x * Tile.TILE_WIDTH, y * Tile.TILE_HEIGHT);
                        bossTiles.Add(Tile.createTile(x, y));
                        break; //create boss
                }
            }
        
        game.arenaWidth = map.GetLength(1) * Tile.TILE_WIDTH;
        game.arenaHeight = map.GetLength(0) * Tile.TILE_HEIGHT;

        game.abyssStartPos = new Vector2(0, 0);
        game.abyssSize = new Vector2(game.arenaWidth, game.arenaHeight);
        game.abyssScaleFactor = new Vector2(2 / game.abyssSize.X, 2 / game.abyssSize.Y);

        //game.camera.setVirtualScreen(game.arenaWidth, game.arenaHeight);
    }

    public void draw2D(Texture2D tileTexture) {
        drawTiles(normalTiles, tileTexture, Color.WHITE);
        drawTiles(breakableTiles, tileTexture, Color.RED);
        drawTiles(bossTiles, tileTexture, Game.Instance.boss.GetColor());
    }

    private void drawTiles(List<Rectangle> tiles, Texture2D tileTexture, Color tint) {
        foreach (Rectangle tile in tiles) {
            Raylib.DrawTexture(tileTexture, (int)tile.x, (int)tile.y, tint);
        }
    }









